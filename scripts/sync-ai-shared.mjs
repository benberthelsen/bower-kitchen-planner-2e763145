/**
 * sync-ai-shared — copies the layout engine into supabase/functions/_shared/layout/
 * rewriting imports for Deno. Run after any change to src/lib/layout:
 *   npm run ai:sync-shared
 * The copies are GENERATED — never edit them directly.
 */
import fs from 'node:fs';
import path from 'node:path';

const SRC = 'src/lib/layout';
const DEST = 'supabase/functions/_shared/layout';
const HEADER = '// GENERATED by scripts/sync-ai-shared.mjs — edit src/lib/layout instead.\n';

fs.mkdirSync(DEST, { recursive: true });

// core.ts = src/types.ts + src/constants.ts merged (engine's '@/' deps)
const typesSrc = fs.readFileSync('src/types.ts', 'utf8');
// Drop the `import { … } from './types'` line — the types are already inlined
// above (typesSrc). Robust to formatting/extension so Deno never sees a bare
// './types' import (which it rejects: "Maybe add a '.ts' extension").
const constantsSrc = fs.readFileSync('src/constants.ts', 'utf8')
  .replace(/^\s*import[^;]*from ['"]\.\/types(?:\.ts)?['"];?\s*$/gm, '');
fs.writeFileSync(path.join(DEST, 'core.ts'), HEADER + typesSrc + '\n' + constantsSrc);

for (const file of fs.readdirSync(SRC)) {
  if (!file.endsWith('.ts')) continue;
  let s = fs.readFileSync(path.join(SRC, file), 'utf8');
  s = s
    .replace(/from '@\/types'/g, "from './core.ts'")
    .replace(/from '@\/constants'/g, "from './core.ts'")
    .replace(/from 'zod'/g, "from 'npm:zod@3.25.76'")
    .replace(/from '\.\/([A-Za-z]+)'/g, "from './$1.ts'");
  // wizardAdapter has no meaning server-side but is harmless; keep everything.
  fs.writeFileSync(path.join(DEST, file), HEADER + s);
}

// ── trade adapter (plan §11.1/§11.2) ────────────────────────────────────────
// promote-ai-design needs the SAME proposal→TradeRoom conversion the client
// tests, so the two directions cannot drift. tradeTypes.ts mirrors
// src/types/trade.ts with its '@/types' dependency pointed at the layout core.
const TRADE_SRC = 'src/lib/trade';
const TRADE_DEST = 'supabase/functions/_shared/trade';
const TRADE_HEADER = '// GENERATED by scripts/sync-ai-shared.mjs — edit src/lib/trade (or src/types/trade.ts) instead.\n';
fs.mkdirSync(TRADE_DEST, { recursive: true });

const tradeTypesSrc = fs.readFileSync('src/types/trade.ts', 'utf8')
  .replace(/from '@\/types'/g, "from '../layout/core.ts'");
fs.writeFileSync(path.join(TRADE_DEST, 'tradeTypes.ts'), TRADE_HEADER + tradeTypesSrc);

for (const file of ['cabinetPlacedItem.ts', 'proposalToTradeRoom.ts']) {
  let s = fs.readFileSync(path.join(TRADE_SRC, file), 'utf8');
  s = s
    // NOTE: '@/types/trade' must be rewritten BEFORE the '@/types' prefix.
    .replace(/from '@\/types\/trade'/g, "from './tradeTypes.ts'")
    .replace(/from '@\/types'/g, "from '../layout/core.ts'")
    .replace(/from '@\/constants'/g, "from '../layout/core.ts'")
    .replace(/from '@\/lib\/layout'/g, "from '../layout/index.ts'")
    .replace(/from '\.\/([A-Za-z]+)'/g, "from './$1.ts'");
  fs.writeFileSync(path.join(TRADE_DEST, file), TRADE_HEADER + s);
}
console.log('synced', fs.readdirSync(DEST).join(', '), '+ trade:', fs.readdirSync(TRADE_DEST).join(', '));
